

const add = (x, y) => {
    return x + y;
}

const subtract = (x, y) => {
    return x - y;
}

const multiply = (x, y) => {
    return x * y;
}

const divide = (x, y) => {
    return x / y;
}

const square = (x) => {
    return x * x;
}

module.exports = {
    add,
    subtract,
    multiply,
    divide,
    square,
}
